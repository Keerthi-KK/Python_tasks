# Assignment Operator 
num = float(input("Enter a number: "))
num += 5
print("After incrementing by 5:", num)

num = float(input("\nEnter a number: "))
num -= 2
print("After decrementing by 2:", num)

num = float(input("\nEnter a number: "))
num *= 2
print("After doubling:", num)

num = float(input("\nEnter a number: "))
num /= 2
print("After halving:", num)

num = int(input("\nEnter a number: "))
num %= 3
print("Remainder when divided by 3:", num)
