import math
print("*************Enter the cylinder values**********************")
radius=int(input("Enter the radius of cylinder: "))
height=int(input("Enter the height of cylinder: "))
Vol1=math.pi*((radius**2)*height)
print("Volume of a cylinder= ",Vol1)
print("***********Enter the cone values***************************")
r2=int(input("Enter the radius of cone:"))
h2=int(input("Enter the height of cone:"))
Vol2=1/3*math.pi*(r2**2)*h2
print("Volume of a cone= ",Vol2)
print("***********Enter the sphere values***************************")
r3=int(input("Enter the radius of sphere:"))
Vol3=4/3*math.pi*(r3**2)
print("Volume of a sphere= ",Vol3)

       

              





